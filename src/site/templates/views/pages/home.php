	<div class="document__contenu--article">

		<p>L’affaire est <em>peut-être</em> là : <a href="#">les droites</a> d’aujourd’hui sont en <strong>train d’inventer</strong> une nouvelle figure du bouc émissaire. Non plus le juif, figure idéale, rassemblant tous les péchés sur sa seule personne, mais une figure plus floue, plus flottante : l’arabe, le musulman (l’islamophobie n’est pas tout à fait le racisme anti-arabes), le rom, le chinois, le travailleur des pays de l’Est européen, l’immigré pauvre ou l’étranger en général, tantôt les mêlant tous dans un même sac, tantôt au contraire jouant les uns contre les autres. L’intérêt d’un tel flottement nouveau serait quadruple : adaptation à un monde ouvert et en mutation permanente où les enjeux de politiques nationale et internationale se mêlent en des amalgames jusque-là insoupçonnés ; protection contre toute accusation de racisme ou de haine, permettant au contraire de rejeter d’avance le soupçon d’antisémitisme sur la gauche et l’extrême gauche ; démantèlement non seulement du front des minorités constitué après-guerre mais de chaque minorité, brutalement divisée entre ses membres choisis et ses membres rejetés (il faut diviser pour régner) ; et dans le même temps récupération de l’idée d’un front des bonnes minorités (juifs, femmes, homosexuels… jusqu’au peuple lui-même, minorité par excellence comme disait Deleuze) contre les mauvaises, suspectes de toutes les abjections, émanations des États où elles seraient majoritaires.</p>

		<p><img src="http://lab.leomartin.net/tem/tem-front/C_jvvBoW0AEMY1Z.jpg_large.jpeg" alt=""></p>

		<p>L’idée que le point commun de toutes les droites européennes d’aujourd’hui serait l’islamophobie est une idée en partie fausse. De fait, une phobie suppose une fixation pathologique sur un objet particulier de répulsion. Or, cela ne semble pas le cas aujourd’hui : les pratiques sont sans rapport avec les discours et les injonctions, ce qui laisse plutôt à penser que les musulmans ne sont pas en vérité l’objet d’une haine, pas plus que les juifs ou les homosexuels ne seraient devenus l’objet d’un amour incongru. L’enjeu véritable n’est pas tant l’islamophobie que cette science nouvelle du bouc émissaire flottant : peu importe qui il est — et cela les gouvernants cyniques l’ont toujours su —, en revanche il importe de ne pas indurer trop longtemps dans la stigmatisation d’une même communauté, d’apprendre à varier, rendre les ressentiments et les frustrations labiles, flexibles, mouvants.</p>

